package com.encapsulation.basics;

public class Sub {
	int a;
	int b;
	public void setAdd(int x,int y) {
		this.a=x;
		this.b=y;
	}
	public int getAdd() {
		return a+b;
	}
	public void setSub(int x,int y) {
		this.a=x;
		this.b=y;
	}
	public int getSub() {
		return a-b;
	
	
	}
}
